-- ==========================================
-- 1. TABELAS DE FILTROS (CATÁLOGOS)
-- ==========================================
CREATE TABLE filtro_idade (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    idade INT
);

CREATE TABLE filtro_categoria (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    categoria VARCHAR(100)
);

CREATE TABLE filtro_modalidade (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    modalidade VARCHAR(100)
);

CREATE TABLE filtro_local (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    local VARCHAR(150)
);

CREATE TABLE filtro_curso (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    curso VARCHAR(120) NOT NULL
);

-- ==========================================
-- 2. TABELA PRINCIPAL DE TURMAS (cursos)
-- ==========================================
CREATE TABLE cursos (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    curso_id INT NOT NULL,
    vagas INT DEFAULT 0,
    vagas_pcd INT DEFAULT 0,
    status VARCHAR(20) DEFAULT 'ativo' CHECK (status IN ('ativo', 'esgotado', 'em_breve', 'encerrado')),
    publicado_em TIMESTAMP NULL,
    inscricoes_abertas_em TIMESTAMP NULL,
    inscricoes_fecham_em TIMESTAMP NULL,
    categoria_id INT NULL,
    modalidade_id INT NULL,
    local_id INT NULL,
    idade_min INT NULL,
    idade_max INT NULL,
    data_inicio DATE NULL,
    data_termino DATE NULL,
    horario_inicio TIME NULL,
    horario_termino TIME NULL,
    descricao TEXT NULL,
    competencias TEXT NULL,
    pre_requisitos TEXT NULL,
    carga_horaria INT NULL,
    nivel_empregabilidade VARCHAR(30) NULL CHECK (nivel_empregabilidade IN ('alta', 'media', 'baixa')),
    video_url VARCHAR(500) NULL,
    faixa_salarial_min NUMERIC(10,2) NULL,
    faixa_salarial_max NUMERIC(10,2) NULL,
    criado_em TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (curso_id) REFERENCES filtro_curso(id),
    FOREIGN KEY (categoria_id) REFERENCES filtro_categoria(id),
    FOREIGN KEY (modalidade_id) REFERENCES filtro_modalidade(id),
    FOREIGN KEY (local_id) REFERENCES filtro_local(id)
);

-- ==========================================
-- 3. TABELA DE USUÁRIOS CADASTRADOS (Módulo 1.1)
-- ==========================================
CREATE TABLE usuarios (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cpf VARCHAR(14) NOT NULL UNIQUE,
    nome VARCHAR(120) NOT NULL,
    email VARCHAR(120) NOT NULL,
    email_confirmacao VARCHAR(120) NULL,
    telefone VARCHAR(20) NOT NULL,
    telefone_alternativo VARCHAR(20) NULL,
    data_nascimento DATE NULL,
    rg VARCHAR(20) NULL,
    genero VARCHAR(30) NULL,
    raca_cor VARCHAR(30) NULL,
    escolaridade VARCHAR(80) NULL,
    cep VARCHAR(12) NULL,
    numero VARCHAR(20) NULL,
    rua VARCHAR(150) NULL,
    bairro VARCHAR(120) NULL,
    municipio VARCHAR(120) NULL,
    uf VARCHAR(2) NULL,
    mora_vitoria BOOLEAN DEFAULT FALSE,
    possui_deficiencia BOOLEAN DEFAULT FALSE,
    tipo_deficiencia VARCHAR(120) NULL,
    recurso_assistivo VARCHAR(200) NULL,
    responsavel_nome VARCHAR(120) NULL,
    responsavel_cpf VARCHAR(14) NULL,
    responsavel_parentesco VARCHAR(40) NULL,
    responsavel_telefone VARCHAR(20) NULL,
    responsavel_email VARCHAR(120) NULL,
    responsavel_autorizacao BOOLEAN DEFAULT FALSE,
    autoriza_uso_imagem BOOLEAN DEFAULT TRUE,
    objetivo_principal VARCHAR(80) NULL,
    objetivo_secundario VARCHAR(80) NULL,
    cpf_documento TEXT NULL,
    rg_documento TEXT NULL,
    criado_em TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    atualizado_em TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- ==========================================
-- 4. TABELA DE PRÉ-INSCRIÇÕES
-- ==========================================
CREATE TABLE pre_inscricoes (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    usuario_id INT NULL,
    curso_id INT NOT NULL,
    nome VARCHAR(120) NOT NULL,
    email VARCHAR(120) NOT NULL,
    telefone VARCHAR(20) NOT NULL,
    cpf VARCHAR(14) NULL,
    rg VARCHAR(20) NULL,
    data_nascimento DATE NULL,
    genero VARCHAR(30) NULL,
    raca_cor VARCHAR(30) NULL,
    escolaridade VARCHAR(80) NULL,
    cep VARCHAR(12) NULL,
    numero VARCHAR(20) NULL,
    rua VARCHAR(150) NULL,
    bairro VARCHAR(120) NULL,
    municipio VARCHAR(120) NULL,
    uf VARCHAR(2) NULL,
    mora_vitoria BOOLEAN DEFAULT TRUE,
    possui_deficiencia BOOLEAN DEFAULT FALSE,
    tipo_deficiencia VARCHAR(120) NULL,
    classificacao VARCHAR(20) DEFAULT 'titular' CHECK (classificacao IN ('titular', 'suplente')),
    status VARCHAR(30) DEFAULT 'inscrito' CHECK (status IN ('inscrito', 'confirmado', 'matriculado', 'concluido', 'nao_concluido', 'desistencia', 'cancelado')),
    posicao_fila INT NULL,
    data_confirmacao TIMESTAMP NULL,
    prazo_confirmacao TIMESTAMP NULL,
    objetivo_principal VARCHAR(80) NULL,
    autoriza_uso_imagem BOOLEAN DEFAULT TRUE,
    cpf_documento TEXT NULL,
    rg_documento TEXT NULL,
    criado_em TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    atualizado_em TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (usuario_id) REFERENCES usuarios(id) ON DELETE SET NULL,
    FOREIGN KEY (curso_id) REFERENCES cursos(id) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE UNIQUE INDEX uk_pre_inscricoes_curso_cpf
ON pre_inscricoes (curso_id, cpf);

-- ==========================================
-- 5. TABELA DE INTERESSADOS (Quiz / Lead)
-- ==========================================
CREATE TABLE interessados (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(100),
    whatsapp VARCHAR(20),
    email VARCHAR(100),
    regiao VARCHAR(50),
    perfil_curso VARCHAR(50),
    status VARCHAR(20) DEFAULT 'aguardando'
);

-- ==========================================
-- 6. SUGESTÕES DE CURSOS (Módulo 4.4)
-- ==========================================
CREATE TABLE sugestoes_cursos (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    usuario_id INT NULL,
    cpf VARCHAR(14) NULL,
    areas_interesse TEXT NULL,
    sugestao_livre TEXT NULL,
    criado_em TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (usuario_id) REFERENCES usuarios(id) ON DELETE SET NULL
);

-- ==========================================
-- 7. QUESTIONÁRIO PÓS-CURSO (Módulo 5.1)
-- ==========================================
CREATE TABLE questionario_conclusao (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pre_inscricao_id INT NOT NULL,
    usuario_id INT NULL,
    curso_id INT NOT NULL,
    conseguiu_emprego VARCHAR(20) NULL CHECK (conseguiu_emprego IN ('sim', 'nao', 'em_processo')),
    contribuiu_profissional INT NULL CHECK (contribuiu_profissional BETWEEN 1 AND 5),
    recomendaria BOOLEAN NULL,
    principal_beneficio TEXT NULL,
    criado_em TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (pre_inscricao_id) REFERENCES pre_inscricoes(id) ON DELETE CASCADE,
    FOREIGN KEY (usuario_id) REFERENCES usuarios(id) ON DELETE SET NULL,
    FOREIGN KEY (curso_id) REFERENCES cursos(id) ON DELETE CASCADE
);

-- ==========================================
-- 8. PESQUISA DE SATISFAÇÃO (Módulo 5.2)
-- ==========================================
CREATE TABLE pesquisa_satisfacao (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pre_inscricao_id INT NOT NULL,
    usuario_id INT NULL,
    curso_id INT NOT NULL,
    nota_instrutor INT NULL CHECK (nota_instrutor BETWEEN 1 AND 5),
    nota_estrutura INT NULL CHECK (nota_estrutura BETWEEN 1 AND 5),
    nota_material INT NULL CHECK (nota_material BETWEEN 1 AND 5),
    nota_geral INT NULL CHECK (nota_geral BETWEEN 1 AND 10),
    comentario TEXT NULL,
    criado_em TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (pre_inscricao_id) REFERENCES pre_inscricoes(id) ON DELETE CASCADE,
    FOREIGN KEY (usuario_id) REFERENCES usuarios(id) ON DELETE SET NULL,
    FOREIGN KEY (curso_id) REFERENCES cursos(id) ON DELETE CASCADE
);

